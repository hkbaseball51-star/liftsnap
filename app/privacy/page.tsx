function Section({ title, children }: { title?: string; children: React.ReactNode }) {
  return (
    <section>
      {title && (
        <h2 className="text-base font-black mb-3 tracking-tight" style={{ color: '#fff' }}>
          {title}
        </h2>
      )}
      <div style={{ color: 'rgba(255,255,255,0.65)' }}>{children}</div>
    </section>
  )
}

function SubSection({ title, children }: { title: string; children: React.ReactNode }) {
  return (
    <div className="mt-4">
      <h3 className="text-sm font-bold mb-2" style={{ color: 'rgba(255,255,255,0.85)' }}>
        {title}
      </h3>
      <div>{children}</div>
    </div>
  )
}

function BulletList({ items }: { items: string[] }) {
  return (
    <ul className="flex flex-col gap-1 pl-3">
      {items.map((item, i) => (
        <li key={i} className="flex gap-2">
          <span style={{ color: '#ED742F', flexShrink: 0 }}>·</span>
          <span>{item}</span>
        </li>
      ))}
    </ul>
  )
}
